using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PlayerKnockOutState : PlayerState
{
    public PlayerKnockOutState(Player _player, PlayerStateMachine _stateMachine, string _animBoolName) : base(_player, _stateMachine, _animBoolName)
    {
    }

    public override void AnimationFinishTrigger()
    {
        base.AnimationFinishTrigger();
    }

    public override void Enter()
    {
        base.Enter();
        player.isAlive = false;
        GameManger.Instance.ChangeCamera();
    }

    public override void Exit()
    {
        base.Exit();
    }

    public override void FixedUpdate()
    {
        base.FixedUpdate();
    }

    public override void Update()
    {
        base.Update();

    }
}
